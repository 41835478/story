        &nbsp;&nbsp;&nbsp;&nbsp;“快来人，快来人啊，少爷割腕自杀了，快来人……”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;……<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;当叶轩醒来的时候，就发现自己躺在一张大床上。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“唔，头好晕……”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;叶轩坐了起来。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;这时，手腕上传来的阵阵剧痛，让他忍不住皱紧了眉头，他凝眸一看，只见自己的左手手腕已经被包成了一个粽子。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“卧槽，这是什么？”叶轩有点搞不明白，但他可以感觉得出，自己左手手腕有伤。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;然而下一刻，他的双眸就是忽然大睁，他感觉到自己的脑袋里忽然多了一大堆东西，是记忆。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;玄阳帝国、南林郡、连云城、叶家少主叶轩……<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;这一股记忆，刺激着他的大脑，让他咬紧牙关，喉咙里发出艰涩的惨叫声。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;足足半晌之后，他额上的青筋才收了回去。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“什么情况，我这是穿越了？”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;叶轩感受着脑海中的那股记忆，急忙爬下床往镜子上一照，竟发现这具身体的主人和他长的一模一样，就连被阳光暴晒的黑色素沉淀都一样。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;他依稀记得，自己在穿越前发生了什么事情。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;当时他还在一个叫做掌阅的网站上看小说，天花板上的电灯忽然开始闪烁，犹如电影中即将有大事发生一样，然而下一刻所发生的事情，就是让他记忆犹新。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;天花板上的电灯忽然熄灭，紧接着，他面前的那台花了大价钱买来的三十寸显示器，竟然开始像吸尘器一样，轰隆隆地往里头吸东西。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;泡面桶、鼠标、键盘、手机、全部被显示器给吸了进去，最后，连叶轩也难以幸免，他可以清晰地听到那嘶嘶直响的电流声，异常刺耳。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“不会真的穿越了吧，还是说我在做梦？……”叶轩有点拿不准。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;正当他准备捏捏自己，看看是否是在做梦的时候，门外忽然有一阵脚步声传来，紧接着闪进来一道娇小的倩影。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“少爷你醒了……”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;说话者是一个年纪大概在十二三岁的少女，她长得亭亭玉立，脸蛋精致得犹如浑然天成。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;叶轩刚转过身来，立即就有一个记忆浮起，这是她的贴身侍女，柳儿。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;还不等叶轩开口，忽然，他的肚子感觉像被万根针刺中一般，这股疼痛突如其来，差点把他给痛晕了过去。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;现在，叶轩可以肯定，自己绝对是穿越了。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“啊……”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;叶轩发出惨叫声。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“少爷你快躺下……”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;柳儿见状，急忙上前把他扶到床铺边上，同时说道：“少爷你的丹田破碎，修为全失，还是现在床上休息一段时间比较好……”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“啥？丹田破碎，修为全失？”叶轩有些诧异，仔细感受了一下，发现自己多了一个内视的能力。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;他感觉到，自己的丹田处空空如也，内心也是有着一股奇妙的空虚感。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“到底什么情况，我穿越成了一个丹田破碎的倒霉蛋，那把我穿越过来做什么？”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;叶轩无奈了。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;不过，他忽然是在自己那破碎的丹田中，发现了一个与众不同的东西，这是一颗金色的圆球。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“丹田的那个缺口，和这金色圆球的大小一致，就是你导致我修为全失么……”叶轩一下子明白了，但尽管他看过不少玄幻小说，也找不到相关的资料。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;这金色圆球，到底是啥？<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;他开始回忆。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;三日前，被取代的倒霉鬼叶轩在叶家后山练拳，偶然捡到了颗金色圆球。接着，金色圆球就是直接闯入了他的丹田中，将他的丹田撞出了一个窟窿来，导致他的灵力外泄，无法凝聚。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;灵力消耗光了还可以运转功法恢复，而且除了丹田之外，武者还可以把灵力藏在各条经脉中。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;但是，这颗金色圆球不仅把他的丹田弄破，更是把他体内的所有灵力以及境界都给吸食殆尽，他也因此沦为一个普通人。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;原本他是叶家的第一天才，年仅十五岁就已经突破到了武道第四重，能够和他相比的屈指可数。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;然而现在，他体内一点灵力都没有，估计连一个叶家的孩童都不如。要知道，叶家人从小就练武，就算是十岁的小孩子都有武道第一重。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;修为全失之后，他就是不断修炼，试图阻止金色圆球吸收灵力，更是服用了大量的天材地宝，但没有任何效果。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;事情传出去之后，整个连云城也是动荡了，因为他已经被一个叫做裂云宗的势力收下，成为外门弟子。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;年后，他就要前往裂云宗报道了。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;裂云宗是一个强大的宗派，比就算十个连云城加起来，恐怕也比不过，而裂云宗之所以收下叶轩，也是因为他的资质不错。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;可现在，叶轩的修为全失，更是无法恢复，裂云宗绝对不会要一个废物弟子的。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;当得知叶轩的修为全失，无法恢复后，整个叶家都是一片死气沉沉的，如果叶轩进入裂云宗，那么叶家也能水涨床高。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;只可惜，叶轩已经成为了一个废人。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;现在，叶轩也是搞清楚是怎么回事了，就是之前的倒霉鬼被这颗金色圆球撞破了丹田，然后受不了打击，割腕自杀。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;而且，还真的自杀成功了。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;接着叶轩就是穿越了过来，取代了那个倒霉鬼。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“少爷你没事吧？”见到叶轩不说话，柳儿也是急忙问道。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“哦，没事，柳儿放心，少爷我不会再做出自杀的事情了，大难不死必有后福。”叶轩认真地说道。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;听到叶轩这么说，柳儿也是喜笑颜开，急忙道：“那你少爷在这休息，柳儿去给你弄一碗补血粥来。”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“嗯。”叶轩点头。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;由于失血过多，因此叶轩只能在自己的床铺上待着，他整整休息了一天的时间。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;这一天里，他也不是没有收获，他首先适应了这具身体，其次懂得运转修炼功法，然后凝聚灵力。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;只不过，这些刚刚凝聚出来的灵力，在流到丹田的时候，就是全部被那颗金色圆球给吸收，一点不剩。而且，就算不被这金色圆球给吸收，也会因为丹田破碎的缘故而溃散掉。<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;“我就不信了，据说这倒霉叶轩之前还是叶家第一天才，他的大伯允许他每个月去宝库领取一株百年血参，我现在就去弄一个来吃吃。”<br/>
        <br/>
        &nbsp;&nbsp;&nbsp;&nbsp;叶轩想着，就是穿好衣服，然后走了出去。
